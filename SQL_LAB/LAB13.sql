-- 25. Display branches where difference between max and min SPI is greater than 1.
SELECT 
    STUDENT_INFO.BRANCH, 
    MAX(RESULT.SPI) - MIN(RESULT.SPI) AS SPI_DIFFERENCE
FROM 
    STUDENT_INFO 
JOIN 
    RESULT ON STUDENT_INFO.RNO = RESULT.RNO
GROUP BY 
    STUDENT_INFO.BRANCH
HAVING 
    (MAX(RESULT.SPI) - MIN(RESULT.SPI)) > 1;